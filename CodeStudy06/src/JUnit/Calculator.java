package JUnit;

public class Calculator {
    /**
     * 加法
     * @param a
     * @param b
     * @return 加所得的值
     */
    public int add(int a ,int b ){
        return a+b;
    }

    /**
     * 减法
     * @param a
     * @param b
     * @return a-b的值
     */
    public int sub(int a ,int b){
        return a-b;
    }
}
