package com.CodeDemo20;

/**
 * 武器类
 */
public class Stacked {
    private String stackName;

    public Stacked(String stackName) {
        this.stackName = stackName;
    }

    public Stacked() {
    }

    public String getStackName() {
        return stackName;
    }

    public void setStackName(String stackName) {
        this.stackName = stackName;
    }
}
